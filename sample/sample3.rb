require '../lib/r_type'

R.run do
  print RType::Matrix.zero 3
  print RType::Matrix.I 3

  a = RType::Matrix[[1, 3], [2, 4]]
  b = RType::Matrix[[0, 2], [1, 3]]

  print t(a)
  print a + b - a * b

  print outer(a, b)
  print kronecker(a, b)

  print a * [1, 2]
  print 1 / a

  x = matrix (1..9).to_a, ncol: 3
  print upper_tri(x)
  print x
end
